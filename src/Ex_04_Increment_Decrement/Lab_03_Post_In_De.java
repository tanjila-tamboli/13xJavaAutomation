package Ex_04_Increment_Decrement;

public class Lab_03_Post_In_De
{
    public static void main(String[] args) {
        int a= 10;
        System.out.println(++a);
        System.out.println(a);

        //post increment
        int a_post= 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
