package Ex_07_WhileLoop;

public class Lab_118_while_true {
    public static void main(String[] args) {
        while(true)
        {
            System.out.println("hello, Tanjila");
        }
    }
    /*This is a while loop with the condition true, which mean The condition will always be true.
        So this loop will run forever — it’s called an infinite loop.*/
}
