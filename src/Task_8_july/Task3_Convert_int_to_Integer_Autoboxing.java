package Task_8_july;

public class Task3_Convert_int_to_Integer_Autoboxing {
    public static void main(String[] args) {
// Declare a primitive int
        int a= 10;
        System.out.println("Primitive value: " + a);
//Convert int to Integer using autoboxing
        Integer b= a;
        System.out.println("Wrapper object:"+b);
    }
}
