package Ex_06_ForLoop;

public class Lab_108_ForLoop_Negative_value {
    public static void main(String[] args) {
        for(int i=-1; i<-10; i--)
        {
            System.out.println(i);
        }
        System.out.println("-------------------");
        for(int i1=0; i1>-10; i1--)
        {
            System.out.println(i1);
        }
    }
}
