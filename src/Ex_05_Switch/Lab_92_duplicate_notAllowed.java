package Ex_05_Switch;

public class Lab_92_duplicate_notAllowed {
    public static void main(String[] args) {
        // duplicate case not allowed in switch
        int a=65;
        switch(a)
        {
            case 1:
                System.out.println("1");
                break;

              //  case 1;
                //    System.out.println("1");
        }
    }
}
