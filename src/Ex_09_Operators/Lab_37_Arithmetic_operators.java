package Ex_09_Operators;

public class Lab_37_Arithmetic_operators {
    public static void main(String args[])
    {
        int a=5;
        float b=3.14f;
        int c = 85;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a-c);
        System.out.println(a*c);

        System.out.println("a+b");
    }

}
